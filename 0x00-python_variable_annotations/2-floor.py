#!/usr/bin/env python3
"""
A type-annotated function floor which takes a float n
as argument and returns the floor of the float.
"""


def floor(n: float) -> int:
    """
    Returns the floor (rounded down to the nearest integer) of a given float.

    Args:
        n (float): The input float.

    Returns:
        int: The floor of the input float, rounded down to the nearest integer.
    """
    return int(n)
